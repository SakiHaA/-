public class VariableSample1 {
	
	public static void main(String[] args) {
		// 変数宣言
		String name;  //名前
		int age;  //年齢
		String hobby;  //趣味
		
		//変数に代入
		name = "YMD";
		age = 20;
		hobby = "サッカー";
		
		System.out.println("初めまして" + name + "です。");
		System.out.println("年齢は" + age + "です。");
		System.out.println("趣味は" + hobby + "です。");
	}

}
